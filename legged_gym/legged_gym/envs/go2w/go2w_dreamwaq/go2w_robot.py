    def _get_noise_scale_vec(self, cfg):
        """Build observation noise scales from observation manager terms."""
        self.add_noise = self.cfg.noise.add_noise
        from legged_gym.managers import ObservationManager

        observation_manager = ObservationManager(self, getattr(cfg, "observations", None))
        manager_noise_vec = observation_manager.compute_noise_scale_vec()
        if manager_noise_vec is not None:
            if manager_noise_vec.numel() != self.num_obs:
                raise ValueError(
                    f"Observation noise vector has {manager_noise_vec.numel()} entries, "
                    f"but cfg.env.num_observations is {self.num_obs}."
                )
            return manager_noise_vec
        return torch.zeros_like(self.obs_buf[0])

